package org.wso2.carbon.apim.betaorder.model;

import org.wso2.carbon.apimgt.usage.publisher.dto.PublisherDTO;

/**
 * Data transfer object for beta order publisher data
 */
public class BetaOrderPublisherDTO extends PublisherDTO {
    String customerId;
    String itemId;
    String orderID;
    Long quantity;
    Long timestamp;

    public String getCustomerId() {
        return customerId;
    }

    public void setCustomerId(String customerId) {
        this.customerId = customerId;
    }

    public String getItemId() {
        return itemId;
    }

    public void setItemId(String itemId) {
        this.itemId = itemId;
    }

    public Long getQuantity() {
        return quantity;
    }

    public void setQuantity(Long quantity) {
        this.quantity = quantity;
    }

    public String getOrderID() {
        return orderID;
    }

    public void setOrderID(String orderID) {
        this.orderID = orderID;
    }

    public Long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(Long timestamp) {
        this.timestamp = timestamp;
    }
}
